public void generartabla()
{
    
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("finalPU");
        UsuarioJpaController controlador = new UsuarioJpaController(emf);
        
  List <Retornomainmentu> j = controlador.mainmenu(this.nick);
  
   for (Retornomainmentu k : j)
        {
            System.out.println(k.getId()+"\t"+k.getSuma()+"\n");
        }
 
        Object[][] rowData = new Object[j.size()][2];
   int voy=0;
   for(int a=0;a<j.size();a++)
   {
       for (int b=0; b<2;b++)
       {
           if(b%2==0)
           {
           rowData[a][b]=j.get(voy).getId();    
           }
           else
           {
            rowData[a][b]=j.get(voy).getSuma();
           }
           
       }
       voy++;
   }
   Object[] names = {"grupo","debt"};
   DefaultTableModel model = new DefaultTableModel( rowData , names);
                this.jTable1.setModel( model );
                if (jTable1.getColumnModel().getColumnCount() > 0) {
            jTable1.getColumnModel().getColumn(1).setResizable(false);
        }
                repaint();
}
